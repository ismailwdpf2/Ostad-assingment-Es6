// Step 1: Create the calculateSquare arrow function
const calculateSquare = (num) => num * num;

// Step 2: Call the calculateSquare function with different numbers
const number1 = 5;
const number2 = 8;
const number3 = 12;

const square1 = calculateSquare(number1);
const square2 = calculateSquare(number2);
const square3 = calculateSquare(number3);

// Step 3: Print the results to the console
console.log("Square of", number1, "is", square1);
console.log("Square of", number2, "is", square2);
console.log("Square of", number3, "is", square3);
